			<h1 name="news" id="news" style="font-family: 'Trebuchet MS', Verdana, sans-serif; font-size: 15px; color: #7c2001;"><a style="color: #7c2001;text-decoration: none;">lynda.com</a> in the Classroom: The Other Teacher </h1>

			<p><div style="float: left;margin-right: 12px;margin-bottom: 7px;width: 200px;color: #666; font-size: 10px; line-height: 14px; font-family: Verdana, sans-serif;"><img src="http://files2.lynda.com/files/lol_email/nlmu/BillBainLynda.jpg" alt="" style="" border="0" /><br />Bill Bain of Full Sail University with <a style="color: #666666;text-decoration: none;">lynda.com</a> cofounder<br />Lynda Weinman.</div> As Senior Director of Education at <a style="color: #000000;text-decoration: none;">lynda.com</a>, I make site visits to many of our Academic Accounts and see how the Online Training Library&reg; is used and implemented by instructors throughout the US. In May, when Lynda was asked to speak to the student body at <a href="http://www.fullsail.edu/?lpk2=757&utm_source=LDCemail&utm_medium=email&utm_content=AcademicNewsletter061510&utm_campaign=AcademicNewsletter" style="color: #000000;text-decoration: underline;">Full Sail University</a>, I met with Bill Bain, Program Director of the Web Design and Development Bachelor of Science Degree, and we discussed the benefits of <a style="color: #000000;text-decoration: none;">lynda.com</a>&rsquo;s Online Training Library&reg; in the classroom and its impact on student success.</p>   
			
			<p>Most universities and colleges have a slate of course resources considered to be the standard learning tools for instructing students.</p>  
			
			<p>&ldquo;In reviewing courses,&rdquo; Bill told me, &ldquo;disciplines don&rsquo;t change, technologies change,&rdquo;</p>
			  
			<p>At Full Sail University, the resources and tools adopted in the classroom include:</p>
			
			<ul style="clear: both;">
				<li>Lecture-Slides in PowerPoint or Keynote and its corresponding follow-up activities</li>
				<li>Lab Challenges&mdash;Activities where students are given assets and then create a project in the computer lab based on a lecture or Online Training Library&reg; movie concept(s)</li>
				<li>Textbooks</li>
				<li><a style="color: #000000;text-decoration: none;">lynda.com</a> Online Training Library&reg;&mdash;Corresponding courses, chapters and/or movies for content area subject</li>
				<li>Online educational and industry-related resources</li>
			</ul>
			
			<p>However, as an ardent and long-time user of <a style="color: #000000;text-decoration: none;">lynda.com</a> in the classroom just as I was at Pasadena City College, Bill at Full Sail has seen results that go beyond the traditional classroom resources. We excitedly noted our top ten best practices and student outcomes when using the Online Training Library&reg; as an on-going instructional resource. </p>
			<ol>
				<li><strong>Basis for student-driven work:</strong> <a style="color: #000000;text-decoration: none;">lynda.com</a> courses provide the basis for project-based work in the classroom. Example: Laurie Burruss&rsquo; <a href="http://www.lynda.com/home/DisplayCourse.aspx?lpk2=757&utm_source=LDCemail&utm_medium=email&utm_content=AcademicNewsletter061510&utm_campaign=AcademicNewsletter" style="color: #000000;text-decoration: underline; font-style: italic;">Web Site Planning and Wireframing: Hands-On Training</a> serves as the basis for students to learn to build a home page like the blueprint for a house. The students are then asked to design a virtual space.</li>
				<li><strong>Guaranteed Currency:</strong> The <a style="color: #000000;text-decoration: none;">lynda.com</a> Online Training Library&reg; is always more current than a published textbook. It&rsquo;s up to date with the latest versions of software and industry-specific topics and practices.</li>
				<li><strong>Promotion of classroom discussion:</strong> <a style="color: #000000;text-decoration: none;">lynda.com</a> authors provide and validate other authentic voices and points-of-view in the classroom. Students can explore and research new topics not taught in the classroom and bring them back to the other students. At Full Sail, this practice is called <cite>Link and Learn</cite>.</li>
				<li><strong>Source for research and design projects:</strong> The Online Training Library&reg; extends learning beyond the classroom and projects at hand. The library allows motivated students to go beyond, to set their work apart with exemplary practices.</li>
				<li><strong>Learning as you need to know:</strong> Students select courses that fit their personal needs and become the drivers of their learning paths. Students with less experience have the ability to watch tutorials to help make up for their lack of experiences. The library provides a solution for every student at the right level&mdash;to fill a gap or to go beyond the scope of an assignment. It promotes student-centric learning.</li>
				<li><strong>Repetitive learning:</strong> The Online Training Library&reg; movies reinforce the classroom and studio experience, allowing a student to watch a chapter as many times as necessary anonymously.</li>
				<li><strong>Missed materials:</strong> It provides a way for students to make up or review materials covered in the classroom.</li>
				<li><strong>Assignments by Instructor:</strong> Instead of reading a chapter, a student can be assigned a movie to watch, follow and do&mdash;providing active and engaged learning.</li>
				<li><strong>Creative processes demonstrated:</strong> Creative Inspirations documentaries provide a glimpse into contemporary design practitioners, their design workflows, and their workplaces. These movies give students insight into the industry level expectations in a real world context.</li>
				<li><strong>Instructional Aid:</strong> The Online Training Library&reg; provides an additional resource for the student who needs help outside the classroom and/or has varying degrees of experience. </li>
			</ol>
			 
			<p>With technologies changing all the time, the Online Training Library&reg; allows the instructor to remain the expert who teaches mastery of a subject, critical problem-solving, and thought leadership to students, while providing the tutorials and skills and tools training necessary to understand the technologies.</p>

			<h1 name="news" id="news" style="font-family: 'Trebuchet MS', Verdana, sans-serif; font-size: 15px; color: #7c2001;">New AIGA Documentary</h1>
<p><a href="http://files2.lynda.com/files/lol_email/trailers/trailer56535.html?utm_source=LDCemail&utm_medium=email&utm_content=NL061510_LL&utm_campaign=AcademicNewsletter"><img src="http://files.lynda.com/files/images/courses/56535_Trailer.jpg" alt="" style="float: left;margin-right: 12px;margin-bottom: 7px;" border="0" /></a>
As a long-time member of AIGA and newly elected member of its national board of directors, <a style="color: #000000;text-decoration: none;">lynda.com</a> founder Lynda Weinman was invited to attend the annual design awards gala in New York City last fall. A few days before the event, Lynda spent some time getting to know some of AIGA&rsquo;s key members and touring the organization&rsquo;s offices and archives, and brought our documentary crew along. The result is our latest documentary, <a href="http://www.lynda.com/home/DisplayCourse.aspx?lpk2=56535&utm_source=LDCemail&utm_medium=email&utm_content=AcademicNewsletter061510&utm_campaign=AcademicNewsletter" style="font-style: italic;color: #000000;text-decoration: underline;">lynda.com Presents AIGA</a>.</p>

