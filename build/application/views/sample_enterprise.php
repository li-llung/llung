								<p style="clear:both;margin-top: 12px; line-height: 18px;"><b>Writing Interesting Tweets</b><br />
									from: <cite><a style="color: #000; text-decoration: underline;" href="http://www.lynda.com/home/DisplayCourse.aspx?lpk2=61687&utm_source=LDCemail&utm_medium=email&utm_content=NL061510_hottip_text&utm_campaign=EnterpriseNewsletter">Twitter Essential Training</a></cite><br />
									with Maria Langer</p>
									<img alt="illustration" style="float:right;margin:4px 0 12px 12px;" src="http://files2.lynda.com/files/lol_email/newsletter/june10/0610newsHOTtip.jpg" />
									<!--<p>&ldquo;Writing Interesting Tweets&rdquo;<br />-->			
			<p>Although Twitter&rsquo;s tag line is "Discover what&rsquo;s happening right now, anywhere in the world," if what&rsquo;s happening isn&rsquo;t all that interesting, do you really need to share it? </p>
			
			<p>Trivial tweets such as &ldquo;I am eating a sandwich,&rdquo; or &ldquo;My cat is sleeping&rdquo; are usually <em>not</em> considered interesting statements and will not build relationships with followers, whether your intention is to communicate to friends, professional colleagues, or clients.</p>
			
			<p>Quality is far more important than quantity on Twitter. You can send 50 tweets a day or more to your Twitter account, but unless a good percentage of them are interesting, no one interesting will follow you. If they do, they&rsquo;ll usually drop you because you use up too much bandwidth without sharing anything worth reading &mdash; like background noise in an already crowded room.</p>
			
			<p>What <em>is</em> interesting? That&rsquo;s different for everyone.  Start with what interests you.  Write updates you would want to read. Post links to content you think your followers might find interesting.  Share photos you find interesting, creative, or beautiful, if that is relevant to your message.</p>
			
			<p>When you&rsquo;re new to Twitter, you may not be able to think of good content to post in Twitter updates. Post a few updates. Follow a few people. Get an idea for what&rsquo;s interesting, then start sharing your comments.</p>
			<p>Remember quality is more important than quantity. Soon you will be tweeting what your followers and others want to read. </p>


